const endPoint = "https://newsapi.org/v2";

//--------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------
//	A P I K E Y S
//
//--------------------------------------------------------------------------------------------------------
 const API_KEY = 'Enter Your Own Api Key with https://newsapi.org/'

//--------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------
//	F E T C H E R
//
//--------------------------------------------------------------------------------------------------------

const fetcher = (query: string) =>
  fetch(`${endPoint}/everything?q=${query}&apiKey=${API_KEY}`).then((res) =>
    res.json()
  );

export { fetcher };
